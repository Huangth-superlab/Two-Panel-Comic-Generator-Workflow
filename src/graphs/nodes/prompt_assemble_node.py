from langchain_core.runnables import RunnableConfig
from langgraph.runtime import Runtime
from coze_coding_utils.runtime_ctx.context import Context
from graphs.state import PromptAssembleInput, PromptAssembleOutput

def prompt_assemble_node(state: PromptAssembleInput, config: RunnableConfig, runtime: Runtime[Context]) -> PromptAssembleOutput:
    """
    title: 提示词组装
    desc: 将场景描述和句子组装到固定的提示词模板中
    integrations: 
    """
    ctx = runtime.context
    
    # 固定的提示词模板（不生成文字，预留文字区域）
    prompt_template = f"""双格垂直治愈系漫画插画，主角是可爱的粉色小猪。

【整体风格】：
- 彩绘铅笔风格，彩色铅笔绘制
- 柔和温暖的色彩（粉、白、淡黄色调）
- 精致的铅笔质感和纸张纹理
- 粗黑边框的格子框架
- 手绘感，笔触自然流畅
- 3:4垂直比例
- 画面具有真实感和高级艺术感
- 整体插画风格统一（角色和背景必须匹配）

【风格一致性要求】：
- 背景必须使用与角色相同的彩绘铅笔风格
- 彩色铅笔的质感，具有真实的笔触纹理和纸张质感
- 色彩细腻柔和，具有层次感和叠加效果
- 背景禁止使用3D写实或照片级渲染
- 背景禁止使用3D渲染、光线追踪或超写实效果
- 背景必须匹配角色手绘的铅笔艺术风格
- 使用彩色铅笔的细腻笔触、色彩叠加和柔和阴影（与角色一致）
- 避免使用与角色风格不匹配的粗糙效果或数字渲染
- 整个插画应该看起来像统一的彩绘铅笔艺术品，具有高级艺术感

【角色设定】：
- 可爱的粉色小猪，圆滚滚的身体，短小的四肢，小耳朵，卷曲的尾巴
- 彩色铅笔绘制的柔和粉色毛发，具有细腻的笔触质感
- 使用参考图保持角色一致性
- 两格中角色设计保持一致
- 铅笔笔触自然，色彩柔和，具有真实感

【上格画面】：
- 场景：{state.upper_scene}
- 氛围：温暖治愈

【下格画面】：
- 场景：{state.lower_scene}
- 与上格的关系：{state.relationship}
- 氛围：温暖治愈

【画面细节】：
- 每个格子用粗黑边框包围
- 彩色铅笔的细腻笔触，整体明亮温暖
- 清晰的铅笔线条，彩绘铅笔艺术风格
- 色彩细腻柔和，具有层次感和叠加效果
- **每格画面的上1/4部分必须保持干净和空旷，不放置任何角色、物品或装饰元素，仅保留纯净的背景色或极简纹理，为后续添加文字预留充足空间**
- 背景元素应该使用彩绘铅笔风格，以匹配角色风格
- 具有真实感和高级艺术感

【禁止内容】：
- 严格禁止：图像中任何地方都不得出现文字、单词或字符
- 严格禁止：不得出现水印、标志或品牌标识
- 严格禁止：不得出现"AI generated"、"豆包AI生成"或任何类似的文字标签
- 严格禁止：不得出现3D写实背景、照片级效果或超写实纹理
- 严格禁止：不得出现彩绘铅笔风格与数字渲染的混搭
- 仅生成视觉插画元素

【氛围】：
- 治愈、放松、柔和可爱
- 微妙的情感共鸣
- 高级艺术感，真实而温暖

注意：生成精致的彩绘铅笔风格插画，具有真实感和高级艺术感，不包含任何文字、水印、标志或3D写实元素。角色和背景必须使用相同的统一彩绘铅笔风格。**每格画面的上1/3区域必须保持干净和空旷，仅用于后续添加文字，不要在该区域放置任何视觉元素。**"""
    
    return PromptAssembleOutput(final_prompt=prompt_template)
